#!/usr/bin/env node
/**
 * Regenerates the SFCC resource bundles under
 *   src/app_airwallex/cartridge/templates/resources/
 * from the i18next JSON source-of-truth under
 *   src/app_airwallex/i18n/translations/
 *
 * The JSON files are managed by the Airwallex i18n service
 * (https://airwallex.atlassian.net/wiki/spaces/FRONT/pages/2904755695);
 * the .properties files are generated and should not be hand-edited.
 */

const fs = require('fs');
const path = require('path');

const TRANSLATIONS_DIR = path.resolve(__dirname, '../src/app_airwallex/i18n/translations');
const RESOURCES_DIR = path.resolve(__dirname, '../src/app_airwallex/cartridge/templates/resources');
const BUNDLE_NAME = 'airwallex';

// Maps the i18n-service JSON filename to the SFCC resource bundle suffix.
// en.json is the default bundle (no locale suffix).
const LOCALE_TO_BUNDLE_SUFFIX = {
  en: '',
  ja: '_ja',
  'zh-CN': '_zh_CN',
  he: '_he',
};

function flatten(obj, prefix = '', out = {}) {
  for (const [key, value] of Object.entries(obj)) {
    // Skip TMS metadata keys. The awx-i18n-service adds a "_comment" marker
    // to pulled files; "_description" is used to give translators context
    // (see i18n doc FAQ #5) and should not appear in runtime .properties.
    if (key === '_comment' || key === '_description') continue;
    const nextKey = prefix ? `${prefix}.${key}` : key;
    if (value && typeof value === 'object' && !Array.isArray(value)) {
      // i18n-service pattern: { "_description": "...", "_value": "..." }
      // - treat _value as the translation string for the parent key.
      if (typeof value._value === 'string') {
        out[nextKey] = value._value;
        continue;
      }
      flatten(value, nextKey, out);
    } else {
      out[nextKey] = String(value);
    }
  }
  return out;
}

// Minimal .properties value escaping. SFCC reads these as UTF-8 so we do
// not unicode-escape non-ASCII characters - keeping Japanese/Chinese/Hebrew
// readable in source.
function escapeValue(value) {
  return value.replace(/\\/g, '\\\\').replace(/\n/g, '\\n').replace(/\r/g, '\\r').replace(/\t/g, '\\t');
}

function renderProperties(flat) {
  const lines = [
    '# AUTO-GENERATED by scripts/i18n-to-properties.js - DO NOT EDIT MANUALLY.',
    '# Source of truth: src/app_airwallex/i18n/translations/*.json',
    '',
  ];
  let prevGroup = null;
  for (const key of Object.keys(flat)) {
    const group = key.split('.', 1)[0];
    if (prevGroup !== null && group !== prevGroup) {
      lines.push('');
    }
    lines.push(`${key}=${escapeValue(flat[key])}`);
    prevGroup = group;
  }
  return lines.join('\n') + '\n';
}

function isGeneratedBundle(filename) {
  return filename === `${BUNDLE_NAME}.properties` || (filename.startsWith(`${BUNDLE_NAME}_`) && filename.endsWith('.properties'));
}

function isJsonFile(filename) {
  return filename.endsWith('.json');
}

function main() {
  if (!fs.existsSync(TRANSLATIONS_DIR)) {
    throw new Error(`Translations directory not found: ${TRANSLATIONS_DIR}`);
  }
  fs.mkdirSync(RESOURCES_DIR, { recursive: true });

  for (const filename of fs.readdirSync(RESOURCES_DIR).filter(isGeneratedBundle)) {
    fs.unlinkSync(path.join(RESOURCES_DIR, filename));
  }

  const jsonFiles = fs.readdirSync(TRANSLATIONS_DIR).filter(isJsonFile);
  if (jsonFiles.length === 0) {
    throw new Error(`No JSON translation files found in ${TRANSLATIONS_DIR}`);
  }

  for (const filename of jsonFiles) {
    const locale = path.basename(filename, '.json');
    if (!(locale in LOCALE_TO_BUNDLE_SUFFIX)) {
      const scriptPath = path.relative(process.cwd(), __filename);
      const warning = `[i18n-to-properties] Skipping ${filename}: locale "${locale}" is not mapped. Add it to LOCALE_TO_BUNDLE_SUFFIX in ${scriptPath}.`;
      console.warn(warning);
      continue;
    }
    const json = JSON.parse(fs.readFileSync(path.join(TRANSLATIONS_DIR, filename), 'utf8'));
    const flat = flatten(json);
    const outName = `${BUNDLE_NAME}${LOCALE_TO_BUNDLE_SUFFIX[locale]}.properties`;
    const outPath = path.join(RESOURCES_DIR, outName);
    const relativeOutPath = path.relative(process.cwd(), outPath);
    const keyCount = Object.keys(flat).length;
    fs.writeFileSync(outPath, renderProperties(flat));
    console.log(`[i18n-to-properties] ${filename} -> ${relativeOutPath} (${keyCount} keys)`);
  }
}

main();
